function toggle() {
  document.documentElement.classList.toggle("animated")
}
//document: toda a página visível do cartão

//.documentElement: dentro do document existe um documento html

//.classList: na lista de classes do html...

//.toggle: na lista de classes do html, você vai fazer a opção de toggle (alternar)

